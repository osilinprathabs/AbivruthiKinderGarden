function loadMedia(filter) {
    console.log('Loading media for filter:', filter);
    const staticData = {
        week_activities: [
            { type: "image", src: "img/week_activities/highlight1.jpg" }
        ],
        club_activities: [
            { type: "image", src: "img/club_activities/club1.jpg" }
